class DHCPTestCase(unittest.NoxTestCase):

    def getInterface(self):
        return str(DHCPTestCase)    

    def setUp(self):
        pass 
 
    def tearDown(self):
        pass

    def fullDHCPPacket(self):    
        eth = ethernet(array('B',large_dhcp))   
        udph = eth.find('udp')
        iph  = eth.find('ipv4')
        dhcph = eth.find('dhcp')

        assert(dhcph)
        assert(dhcph.op    == 1)
        assert(dhcph.htype == 1)
        assert(dhcph.hlen  == 6)
        assert(dhcph.hops  == 0)
        assert(dhcph.xid   == 0x9514f72d)
        assert(dhcph.secs  == 0)
        assert(dhcph.flags == 0)
        assert(dhcph.ciaddr == 0)
        assert(dhcph.yiaddr == 0)
        assert(dhcph.siaddr == 0)
        assert(dhcph.giaddr == 0)
        assert(array_to_octstr(dhcph.chaddr[:6]) == '00:1d:09:21:7f:14')
        assert(len(dhcph.parsedOptions.keys()) == 4)

def suite(ctxt):
    suite = pyunit.TestSuite()
    suite.addTest(DHCPTestCase("fullDHCPPacket", ctxt))
    return suite
